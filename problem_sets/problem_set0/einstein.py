# Prompt the user for the value of 'm' and convert it to an integer
m = int(input('m: '))

# Calculate the square of the speed of light 'c' (approximately 300,000,000 meters per second)
c = 300000000**2

# Calculate the energy 'e' using the formula E=mc^2, where 'm' is the mass and 'c' is the speed of light squared
e = m * c

# Print the calculated energy 'e'
print(e)
